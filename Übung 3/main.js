// leeren String für die Ausgabe deklarieren
var ergebnis = "";

// die for-Schleife läuft von i=0 bis i=10
for (var i=0; i<10; i=i+1) {

  // Ziffer aus i zum Ergebnis hinzufügen
  ergebnis = ergebnis + i;

  // aktueller Wert der Variable ergebnis in der Konsole ausgeben
  console.log(ergebnis);
}
